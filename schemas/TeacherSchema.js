const mongoose = require("mongoose");

const teacherSchema = mongoose.Schema({
    host_id: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

module.exports = mongoose.model('Teacher', teacherSchema);